package model;

/**
 *
 * @author Admin
 */
public class Order {
    int id;
    String name;
    float price;
    String date;

    public Order() {
    }

    public Order(int id, String name, float price, String date) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    @Override
    public String toString() {
        return "Order{" + "id=" + id + ", name=" + name + ", price=" + price + ", date=" + date + '}';
    }
    
    
}
